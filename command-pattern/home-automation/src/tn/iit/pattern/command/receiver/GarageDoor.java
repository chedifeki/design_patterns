package tn.iit.pattern.command.receiver;

public class GarageDoor {
    public void up() {
        System.out.println("Garage Door is up!");
    }

    public void lightOn() {
        System.out.println("Garage Light is on!");
    }

    public void down() {
        System.out.println("Garage Door is down!");

    }

    public void LightOff() {
        System.out.println("Garage Light is on!");
    }
}
